class Solution:
	# @param A : list of integers
	# @param B : integer
	# @return an integer
	def removeElement(self, A, B):
		matching = 0
		n = len(A)
		if n == 0:
			return A
		for i in range(n):
			if A[i] == B:
				matching += 1
			elif matching > 0:
				A[i-matching] = A[i]
		A = A[:n-matching]
		return len(A)


